package com.example.baoxiaojianapp.classpakage;

public class AppraisalResult {
    private String appraisalBrand;
    private String appraisalData;
    private String appraisalId;
    private String appraisalImage;
    private int type;
    private String detailModels;


    public String getAppraisalBrand() {
        return appraisalBrand;
    }

    public void setAppraisalBrand(String appraisalBrand) {
        this.appraisalBrand = appraisalBrand;
    }

    public String getAppraisalData() {
        return appraisalData;
    }

    public void setAppraisalData(String appraisalData) {
        this.appraisalData = appraisalData;
    }

    public String getAppraisalId() {
        return appraisalId;
    }

    public void setAppraisalId(String appraisalId) {
        this.appraisalId = appraisalId;
    }

    public int getType() {
        return type;
    }

    public void setType(int type) {
        this.type = type;
    }

    public String getDetailModels() {
        return detailModels;
    }

    public void setDetailModels(String detailModels) {
        this.detailModels = detailModels;
    }

    public String getAppraisalImage() {
        return appraisalImage;
    }

    public void setAppraisalImage(String appraisalImage) {
        this.appraisalImage = appraisalImage;
    }
}
